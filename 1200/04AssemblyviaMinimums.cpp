// ---------- Utility Functions ----------
long long gcd(long long a, long long b) {
    while (b != 0) {
        long long temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}

long long lcm(long long a, long long b) {
    return (a / gcd(a, b)) * b;  // avoid overflow
}

template <typename T>
void printArray(const vector<T>& arr) {
    for (auto &x : arr) cout << x << ' ';
    cout << endl;
}


// ---------- Solve Function ----------
void solve() {

    int n;
    cin >> n;

    int size = (n*(n-1))/2;

    vector<int> arr(size);
    for(int i = 0; i<size; i++) {
        cin >> arr[i];
    }
    // printArray(arr);

    sort(all(arr));

    vector<int> ori;
    int cnt = -1;

    int i = 0;
    while(i < size) {
        ori.pb(arr[i]);
        cnt++;
        int jump = i + (n-cnt - 1);
        i = jump;
    }

    ori.pb(ori[n-2]);

    printArray(ori);
}

