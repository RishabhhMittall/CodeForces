// GCD function
long long gcd(long long a, long long b) {
    while (b != 0) {
        long long temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}

// LCM using the GCD function
long long lcm(long long a, long long b) {
    return (a / gcd(a, b)) * b;  // to avoid overflow
}


