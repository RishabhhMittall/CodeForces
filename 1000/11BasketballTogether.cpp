// ---------- Solve Function ----------
void solve() { 
    int n, d;
    cin >> n >> d;

    vector<int> a(n);
    for(int i = 0; i<n; i++) {
        cin >> a[i];
    }

    sort(all(a));
    int remain = n;
    int ans = 0;

    for(int i = n - 1; i >= 0; i--) {
        int curr = a[i];
        int need = 0;
        if(d % curr == 0) {
            need = d/curr + 1;
        }
        else {
            need = ceil((double)d/curr);
        }

        if(need <= remain) {
            remain -= need;
            ans++;
        }
        else {
            break;
        }
    }
    cout << ans << endl;


}

